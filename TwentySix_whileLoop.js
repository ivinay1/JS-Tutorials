// ITERATORS 
// The while loop executes a block of code as long as a specified condition is true.
// Syntax:
while (condition) {
    // code block to be executed
  }
  
  // Example: Print numbers from 1 to 5
  let i = 1;
  while (i <= 5) {
    console.log(i);
    i++;  // increment the counter
  }
  
  // Note:
  // 1. Be careful of infinite loops. Ensure the condition will eventually become false.
  

  // The do...while loop executes a block of code once before checking the condition.
// Then it repeats the loop as long as the specified condition is true.
// Syntax:
do {
    // code block to be executed
  } while (condition);
  
  // Example: Print numbers from 1 to 5
  let j = 1;
  do {
    console.log(i);
    j++;  // increment the counter
  } while (j <= 5);
  
  // Note:
  // 1. The code block inside the do...while loop is executed at least once, 
  //    even if the condition is false.

 
